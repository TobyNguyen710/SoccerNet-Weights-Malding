def get_legible_images_above_threshold(tracklet_dir: str, pipeline,
                                        images_to_keep: List[str],
                                        threshold: float,
                                        max_images: Optional[int] = None) -> Tuple[List[str], List[Dict]]:
    valid_image_paths = []
    for ext in ['*.jpg', '*.png']:
        for f in Path(tracklet_dir).glob(ext):
            frame_id = f.stem
            if is_image_in_keep_list(frame_id, images_to_keep):
                valid_image_paths.append(str(f))

    if not valid_image_paths:
        return [], []

    scored_images = []
    for img_path in valid_image_paths:
        result = pipeline.classify_image(img_path)
        if result['score'] >= threshold:
            scored_images.append({
                'path': img_path,
                'score': result['score'],
                'frame_id': Path(img_path).stem
            })

    scored_images.sort(key=lambda x: x['score'], reverse=True)

    # ✅ LIMIT TO MAX IMAGES (default 100)
    if max_images and len(scored_images) > max_images:
        scored_images = scored_images[:max_images]

    paths = sort_frames_numerically([img['path'] for img in scored_images])
    details = scored_images

    return paths, details


def score_tracklet_legibility(tracklet_dir: str, tracklet_name: str, outlier_data: dict) -> dict:
    global PRE_RESULTS, GROUND_TRUTH

    result = {
        'tracklet_id': tracklet_name,
        'tracklet_number': extract_tracklet_number(tracklet_name),
        'total_frames': 0,
        'frames_in_keep_list': 0,
        'legible_images': [],
        'legibility_details': [],
        'status': 'success',
        'is_ball_tracklet': False,
        'marked_illegible': False,
        'images_to_keep_count': 0,
        'ground_truth_value': GROUND_TRUTH.get(tracklet_name, '-1'),
    }

    try:
        # ✅ SKIP BALL TRACKLETS (prediction = 0)
        if is_ball_tracklet(tracklet_name, PRE_RESULTS):
            result['status'] = 'skipped_ball_tracklet'
            result['is_ball_tracklet'] = True
            return result

        # ✅ SKIP TRACKLETS WITH GT = -1 (illegible in ground truth)
        gt_value = GROUND_TRUTH.get(tracklet_name, '-1')
        if gt_value == '-1':
            result['status'] = 'skipped_gt_illegible'
            result['marked_illegible'] = True
            return result

        images_to_keep = get_images_to_keep(tracklet_name, outlier_data)
        result['images_to_keep_count'] = len(images_to_keep)

        image_paths = sorted([str(f) for f in Path(tracklet_dir).glob('*.jpg')])
        result['total_frames'] = len(image_paths)

        result['frames_in_keep_list'] = sum(
            1 for img_path in image_paths
            if is_image_in_keep_list(Path(img_path).stem, images_to_keep)
        )

        if result['frames_in_keep_list'] == 0:
            result['status'] = 'no_frames_after_outlier_elimination'
            PRE_RESULTS = mark_tracklet_illegible(tracklet_name, PRE_RESULTS, CONFIG['pre_results_path'])
            result['marked_illegible'] = True
            return result

        legibility_pipeline = load_legibility_pipeline()
        legible_images, details = get_legible_images_above_threshold(
            tracklet_dir,
            legibility_pipeline,
            images_to_keep,
            threshold=CONFIG['legibility_threshold'],
            max_images=CONFIG.get('max_images_per_tracklet')  # ✅ MAX 100
        )

        result['legible_images'] = legible_images
        result['legibility_details'] = details
        result['legible_count'] = len(legible_images)

        if not legible_images:
            result['status'] = 'no_images_above_threshold'
            PRE_RESULTS = mark_tracklet_illegible(tracklet_name, PRE_RESULTS, CONFIG['pre_results_path'])
            result['marked_illegible'] = True

        return result

    except Exception as e:
        result['status'] = 'error'
        result['error'] = str(e)
        PRE_RESULTS = mark_tracklet_illegible(tracklet_name, PRE_RESULTS, CONFIG['pre_results_path'])
        result['marked_illegible'] = True
        return result

