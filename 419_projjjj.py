def extract_tracklet_number(tracklet_name: str) -> int:
    """Extract numeric part from tracklet name for proper sorting."""
    numbers = re.findall(r'\d+', tracklet_name)
    if numbers:
        return int(numbers[0])
    return float('inf')

def extract_frame_number(filename: str) -> int:
    """
    Extract numeric part from filename stem for frame sorting.
    ✅ FIXED: Uses numbers[-1] to get frame ID, not tracklet ID
    e.g., "0_28.jpg" -> stem="0_28" -> numbers=['0','28'] -> returns 28
    """
    stem = Path(filename).stem
    numbers = re.findall(r'\d+', stem)
    if numbers:
        # 🆕 GRAB THE LAST NUMBER (frame ID), not the first (tracklet ID)
        return int(numbers[-1])
    return float('inf')

def sort_tracklets_numerically(tracklets: List[Path]) -> List[Path]:
    """Sort tracklet folders by numeric value"""
    return sorted(tracklets, key=lambda x: extract_tracklet_number(x.name))

def sort_frames_numerically(frames: List[str]) -> List[str]:
    """Sort frame paths by numeric frame ID"""
    return sorted(frames, key=lambda x: extract_frame_number(x))

