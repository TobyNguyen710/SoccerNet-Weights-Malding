# ── Main Cropping Function ────────────────────────────────────────────────────
def crop_torso_from_images(
    image_paths: Union[str, List[str]],
    output_folder: str = 'temp_croped/',
    conf_threshold: float = 0.3,
    padding: float = 0,
    model_path: str = 'yolov8m-pose.pt',
    verbose: bool = True
) -> List[str]:
    """
    Detect human pose in image(s) and crop the torso/back region.

    Parameters
    ----------
    image_paths : str or List[str]
        Path to single image or list of image paths
    output_folder : str
        Directory to save cropped images (will be created if not exists)
    conf_threshold : float
        Confidence threshold for pose detection (default: 0.3)
    padding : float
        Padding ratio around torso bounding box (default: 0.10 = 10%)
    model_path : str
        Path to YOLO pose model
    verbose : bool
        Print progress messages

    Returns
    -------
    List[str]
        List of paths to saved cropped images

    COCO 17-keypoint indices for torso:
        5  = left shoulder,  6  = right shoulder
        11 = left hip,       12 = right hip
    """

    # ── Setup ──────────────────────────────────────────────────────────────────
    load_pose_model(model_path)

    # Normalize input to list
    if isinstance(image_paths, str):
        image_paths = [image_paths]

    # Create output directory
    Path(output_folder).mkdir(parents=True, exist_ok=True)

    # Torso keypoints (COCO format)
    TORSO_KP_INDICES = [5, 6, 11, 12]

    saved_paths = []

    # ── Process Each Image ────────────────────────────────────────────────────
    for idx, img_path in enumerate(image_paths):
        if verbose:
            print(f'\n[{idx+1}/{len(image_paths)}] Processing: {img_path}')

        # Load image
        img_bgr = cv2.imread(img_path)
        if img_bgr is None:
            print(f'  ⚠ Could not load: {img_path} — Skipping')
            continue

        img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
        h, w = img_rgb.shape[:2]

        # Run pose detection
        results = POSE_MODEL(img_rgb, conf=conf_threshold)

        if not results or results[0].keypoints is None:
            print(f'  ⚠ No person detected — Skipping')
            continue

        # Get best detection (highest confidence)
        res = results[0]
        scores = res.boxes.conf.cpu().numpy()
        best_idx = int(np.argmax(scores))
        keypoints = res.keypoints.xy.cpu().numpy()[best_idx]

        if verbose:
            print(f'  ✓ Detected {len(scores)} person(s). Using #{best_idx} (conf={scores[best_idx]:.2f})')

        # ── Compute Torso Bounding Box ────────────────────────────────────────
        torso_pts = keypoints[TORSO_KP_INDICES]

        # Filter out undetected keypoints (0,0)
        valid_mask = (torso_pts[:, 0] > 0) | (torso_pts[:, 1] > 0)
        valid_pts = torso_pts[valid_mask]

        if len(valid_pts) < 2:
            print(f'  ⚠ Not enough torso keypoints visible — Skipping')
            continue

        x_min, y_min = valid_pts.min(axis=0)
        x_max, y_max = valid_pts.max(axis=0)

        # Add padding
        kp_w = x_max - x_min
        kp_h = y_max - y_min
        pad_x = kp_w * padding
        pad_y = kp_h * padding

        x1 = max(0, int(x_min - pad_x))
        y1 = max(0, int(y_min - pad_y))
        x2 = min(w, int(x_max + pad_x))
        y2 = min(h, int(y_max + pad_y))

        # ── Crop and Save ─────────────────────────────────────────────────────
        torso_crop = img_rgb[y1:y2, x1:x2]

        # Generate output filename
        base_name = Path(img_path).stem
        output_path = os.path.join(output_folder, f'{base_name}_torso_crop.png')

        Image.fromarray(torso_crop).save(output_path)
        saved_paths.append(output_path)

        if verbose:
            print(f'  ✓ Saved: {output_path} ({x2-x1} × {y2-y1} px)')

    if verbose:
        print(f'\n✅ Complete! {len(saved_paths)}/{len(image_paths)} images cropped to: {output_folder}')

    return saved_paths


