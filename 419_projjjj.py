# ── ReID model ────────────────────────────────────────────────────────────────

class ReIDExtractor:
    """ResNet50 backbone with L2-normalised global features (Centroids-ReID style)."""

    transform = transforms.Compose([
        transforms.Resize((256, 128)),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225]),
    ])

    def __init__(self, model_path, device='cuda'):
        self.device = device
        self.model = self._build(model_path)

    def _build(self, path):
        model = models.resnet50(weights=None)
        model.fc = nn.Identity()

        if os.path.exists(path):
            try:
                ckpt = torch.load(path, map_location=self.device, weights_only=False)
                sd = ckpt.get('state_dict', ckpt.get('model', ckpt))
                # strip DDP prefix; keep only matching layers
                sd = {k.lstrip('module.'): v for k, v in sd.items()
                      if k.lstrip('module.') in model.state_dict()
                      and model.state_dict()[k.lstrip('module.')].shape == v.shape}
                model.load_state_dict(sd, strict=False)
                print("✓ ReID weights loaded")
            except Exception as e:
                print(f"⚠️  ReID weights failed ({e}), falling back to ImageNet")
                model = models.resnet50(weights=models.ResNet50_Weights.IMAGENET1K_V1)
                model.fc = nn.Identity()
        else:
            print("⚠️  ReID model not found, using ImageNet weights")
            model = models.resnet50(weights=models.ResNet50_Weights.IMAGENET1K_V1)
            model.fc = nn.Identity()

        return model.to(self.device).eval()

    def extract(self, img_path):
        """Return L2-normalised 2048-d vector, or None on failure."""
        try:
            img = self.transform(Image.open(img_path).convert('RGB')).unsqueeze(0).to(self.device)
            with torch.no_grad():
                feat = self.model(img)
                feat = nn.functional.normalize(feat, p=2, dim=1)
            return feat.cpu().numpy().flatten()
        except Exception as e:
            print(f"    ✗ Feature error {img_path}: {e}")
            return None

    def extract_tracklet(self, tracklet_path, tracklet_name):
        """Extract features for every image in a tracklet folder."""
        exts = {'.jpg', '.jpeg', '.png', '.bmp'}
        images = sorted(f for f in os.listdir(tracklet_path)
                        if Path(f).suffix.lower() in exts)

        feats, names = [], []
        for name in tqdm(images, desc=f"  {tracklet_name}", leave=False):
            feat = self.extract(os.path.join(tracklet_path, name))
            if feat is not None:
                feats.append(feat)
                names.append(name)

        if not feats:
            return None

        arr = np.array(feats)
        np.save(os.path.join(REID_FEATURES_DIR, f'{tracklet_name}_features.npy'), arr)
        return arr, names


# ── Gaussian outlier elimination ──────────────────────────────────────────────

def gaussian_outlier_elimination(features, image_names,
                                  threshold=GAUSSIAN_THRESHOLD,
                                  rounds=GAUSSIAN_ROUNDS):
    """
    Iteratively remove frames whose distance from the inlier mean
    exceeds `threshold * std`.  Returns kept/ignored image names + stats.
    """
    n = len(image_names)
    if n <= 2:
        return image_names.copy(), [], {'rounds_run': 0, 'reason': 'too_few'}

    kept_idx = list(range(n))
    round_log = []

    for r in range(rounds):
        if len(kept_idx) <= 2:
            break

        mu = features[kept_idx].mean(axis=0)
        dists = np.linalg.norm(features[kept_idx] - mu, axis=1)
        mu_d, std_d = dists.mean(), dists.std()

        if std_d < 1e-8:
            break

        mask = np.abs(dists - mu_d) <= threshold * std_d
        removed = int((~mask).sum())
        round_log.append({'round': r, 'before': len(kept_idx),
                          'removed': removed, 'std': float(std_d)})
        kept_idx = [i for i, ok in zip(kept_idx, mask) if ok]
        if removed == 0:
            break

    kept_set = set(kept_idx)
    kept   = [image_names[i] for i in range(n) if i in kept_set]
    ignored = [image_names[i] for i in range(n) if i not in kept_set]

    stats = {'initial': n, 'kept': len(kept),
             'removed': len(ignored), 'rounds': round_log}
    return kept, ignored, stats


