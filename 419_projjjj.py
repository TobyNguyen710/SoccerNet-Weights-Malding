class LegibilityPipeline:
    def __init__(self, config: dict):
        self.config = config
        self.processed_data = {}
        self.pre_results = {}
        self.outlier_data = {}
        self.ground_truth = {}

    def initialize(self):
        print(f"\n{'='*70}")
        print(f"📦 CELL 1: LEGIBILITY CLASSIFIER (SEQUENCE MODE) & DATA SETUP")
        print(f"{'='*70}")
        print(f"Dataset:            {self.config['split']}")
        print(f"Images:             {self.config['images_folder']}")
        print(f"Ground Truth:       {self.config['ground_truth_file']}")
        print(f"Output:             {self.config['output_folder']}")
        print(f"Pre-Results:        {self.config['pre_results_path']}")
        print(f"Outlier Elimination:{self.config['outlier_elimination_file']}")
        print(f"Seq Window:         {self.config['sequence_window']} frames")
        print(f"Top K Sequences:    {self.config['top_k_sequences']}")
        print(f"{'='*70}\n")

        print(f"🔧 Loading Models...")
        load_legibility_pipeline()
        load_pre_results_global()
        load_outlier_data_global()

        self.pre_results = PRE_RESULTS
        self.outlier_data = OUTLIER_DATA

        print(f"\n📖 Loading Ground Truth...")
        load_ground_truth(self.config['ground_truth_file'])
        self.ground_truth = GROUND_TRUTH

        return True

    def run(self):
        if not self.initialize():
            return None

        images_path = Path(self.config['images_folder'])
        tracklets = [d for d in images_path.iterdir() if d.is_dir()]

        # 🆕 SORT TRACKLETS NUMERICALLY
        tracklets = sort_tracklets_numerically(tracklets)

        print(f"\n📁 Tracklet order (first 10): {[t.name for t in tracklets[:10]]}")

        if self.config['max_tracklets']:
            tracklets = tracklets[:self.config['max_tracklets']]

        ball_tracklets = [t for t in tracklets if is_ball_tracklet(t.name, self.pre_results)]
        processable_tracklets = [t for t in tracklets if not is_ball_tracklet(t.name, self.pre_results)]

        print(f"\n📦 Scoring {len(processable_tracklets)} tracklets for legibility sequences...")
        print(f"   ⚽ Skipping {len(ball_tracklets)} ball tracklets (already in pre_results.json)")
        print(f"   📋 Using images_to_keep from outlier_elimination.json\n")

        results = []
        illegible_count = 0
        total_legible_images = 0
        total_sequences = 0

        for tracklet in tqdm(tracklets, desc="Scoring legibility"):
            if self.config['verbose']:
                print(f"\n{'─'*50}")
                print(f"TRACKLET {tracklet.name} (#{extract_tracklet_number(tracklet.name)})")
                print(f"{'─'*50}")

            result = score_tracklet_legibility(
                str(tracklet),
                tracklet.name,
                self.outlier_data
            )
            result['ground_truth'] = self.ground_truth.get(tracklet.name, '-1')
            results.append(result)
            self.processed_data[tracklet.name] = result

            total_legible_images += result.get('legible_count', 0) if 'legible_count' in result else len(result.get('legible_images', []))
            total_sequences += result.get('sequences_found', 0)

            if result['marked_illegible']:
                illegible_count += 1

            if self.config['verbose']:
                if result['status'] == 'skipped_ball_tracklet':
                    print(f"   ⚽ Skipped (ball tracklet)")
                elif result['status'] == 'insufficient_frames_for_sequence':
                    print(f"   ⚠️  Insufficient frames for sequence")
                elif result['status'] == 'no_legible_sequences':
                    print(f"   ⚠️  No legible sequences found")
                else:
                    print(f"   ✅ {result['sequences_found']} sequences identified ({len(result['legible_images'])} unique images)")

        # Save legibility report
        legibility_report = {
            'timestamp': datetime.now().isoformat(),
            'config': self.config,
            'tracklets_processed': len(results),
            'tracklets_skipped_balls': len(ball_tracklets),
            'tracklets_marked_illegible': illegible_count,
            'total_sequences_found': total_sequences,
            'total_legible_images': total_legible_images,
            'tracklet_order': [r['tracklet_id'] for r in results],
            'legible_images_per_tracklet': {
                r['tracklet_id']: r.get('legible_images', [])
                for r in results if r['status'] not in ['skipped_ball_tracklet', 'insufficient_frames_for_sequence', 'no_legible_sequences']
            },
            'sequence_details_per_tracklet': {
                r['tracklet_id']: r.get('sequence_details', [])
                for r in results if r.get('sequence_details')
            }
        }

        report_path = Path(self.config['output_folder']) / 'legibility_report.json'
        with open(report_path, 'w') as f:
            json.dump(legibility_report, f, indent=2, default=str)

        print(f"\n{'='*70}")
        print(f"📊 LEGIBILITY SCORING COMPLETE (SEQUENCE MODE)")
        print(f"{'='*70}")
        print(f"   Total tracklets:              {len(tracklets)}")
        print(f"   Ball tracklets skipped:       {len(ball_tracklets)}")
        print(f"   Illegible tracklets:          {illegible_count}")
        print(f"   Processed tracklets:          {len(processable_tracklets) - illegible_count}")
        print(f"   Total Sequences Found:        {total_sequences}")
        print(f"   Total Unique Images:          {total_legible_images}")
        print(f"\n✅ Legibility scores saved to: {report_path}")
        print(f"📄 Pre-results updated: {self.config['pre_results_path']}")
        print(f"\n📝 Ready for Cell 2 (Image Processing, Posing & Cropping)\n")

        return self.processed_data

