total_enhanced = 0
total_cropped = 0
failed_enhance = 0
failed_crop = 0
num_batches = ceil(total_images / CONFIG['batch_size'])

mode = "⚡ LITE+Deblur" if CONFIG['skip_enhancement'] and CONFIG.get('skip_with_deblur', False) else "⚡ LITE" if CONFIG['skip_enhancement'] else "🔧 FULL"

for batch_idx in tqdm(range(num_batches), desc=f"{mode} Pipeline", disable=False):
    start_idx = batch_idx * CONFIG['batch_size']
    end_idx = min(start_idx + CONFIG['batch_size'], total_images)
    batch_images = all_images[start_idx:end_idx]

    images, tracklet_ids, frame_ids = [], [], []
    for img_info in batch_images:
        try:
            img = cv2.imread(img_info['path'])
            images.append(cv2.cvtColor(img, cv2.COLOR_BGR2RGB) if img is not None else None)
            tracklet_ids.append(img_info['tracklet_id'])
            frame_ids.append(img_info['frame_id'])
        except:
            images.append(None)
            tracklet_ids.append(img_info['tracklet_id'])
            frame_ids.append(img_info['frame_id'])

    enhanced_paths = enhance_batch(images, tracklet_ids, frame_ids)
    total_enhanced += sum(1 for p in enhanced_paths if p is not None)
    failed_enhance += sum(1 for p in enhanced_paths if p is None)

    valid_enhanced = [(p, t, f) for p, t, f in zip(enhanced_paths, tracklet_ids, frame_ids) if p is not None]
    if valid_enhanced:
        valid_paths = [p for p, t, f in valid_enhanced]
        valid_tids = [t for p, t, f in valid_enhanced]
        valid_fids = [f for p, t, f in valid_enhanced]
        try:
            cropped_paths = crop_batch(valid_paths, valid_tids, valid_fids)
            total_cropped += len(cropped_paths)
            failed_crop += len(valid_paths) - len(cropped_paths)
        except:
            failed_crop += len(valid_paths)

print(f"✅ Done | Enhanced: {total_enhanced} | Cropped: {total_cropped} | Failed: {failed_crop}")

