def identify_soccer_balls(image_dir):
    """
    Check tracklets and identify which ones contain soccer balls
    based on image dimensions (small crops = soccer balls)

    Directory structure expected:
    image_dir/
        0/
            image1.jpg
            image2.jpg
            ...
        1/
            image1.jpg
            image2.jpg
            ...
        2/
            ...
    """
    ball_list = []

    # Get all numbered tracklet folders (0, 1, 2, 3, ...)
    tracklet_folders = [f for f in os.listdir(image_dir) if os.path.isdir(os.path.join(image_dir, f))]
    tracklet_folders.sort(key=lambda x: int(x) if x.isdigit() else 0)  # Sort numerically

    print(f"Found {len(tracklet_folders)} tracklet folders in {image_dir}")

    for track in tqdm(tracklet_folders, desc="Processing tracklets"):
        track_path = os.path.join(image_dir, track)

        # Get all images in this tracklet folder
        image_names = [f for f in os.listdir(track_path) if f.lower().endswith(('.jpg', '.jpeg', '.png', '.bmp'))]

        if len(image_names) == 0:
            continue

        # Sample images for analysis
        sample = min(len(image_names), SAMPLE_SIZE)
        imgs = np.random.choice(image_names, size=sample, replace=False)

        width_list = []
        height_list = []

        for img_name in imgs:
            img_path = os.path.join(track_path, img_name)
            img = cv2.imread(img_path)
            if img is not None:
                h, w = img.shape[:2]
                width_list.append(w)
                height_list.append(h)

        if len(width_list) > 0 and len(height_list) > 0:
            mean_w, mean_h = np.mean(width_list), np.mean(height_list)
            if mean_h <= HEIGHT_MIN and mean_w <= WIDTH_MIN:
                # this must be a soccer ball
                ball_list.append(track)
                print(f"  Tracklet {track}: Ball detected (mean_h={mean_h:.2f}, mean_w={mean_w:.2f})")

    return ball_list

