class LegibilityPipeline:
    def __init__(self, config: dict):
        self.config = config
        self.processed_data = {}
        self.pre_results = {}
        self.outlier_data = {}
        self.ground_truth = {}

    def initialize(self):
        print(f"🚀 Legibility Pipeline (Threshold: {self.config['legibility_threshold']}, Max Images: {self.config['max_images_per_tracklet']})")

        load_legibility_pipeline()
        load_pre_results_global()
        load_outlier_data_global()
        self.pre_results = PRE_RESULTS
        self.outlier_data = OUTLIER_DATA
        load_ground_truth(self.config['ground_truth_file'])
        self.ground_truth = GROUND_TRUTH
        return True

    def run(self):
        if not self.initialize():
            return None

        images_path = Path(self.config['images_folder'])
        tracklets = [d for d in images_path.iterdir() if d.is_dir()]
        tracklets = sort_tracklets_numerically(tracklets)

        if self.config['max_tracklets']:
            tracklets = tracklets[:self.config['max_tracklets']]

        # ✅ Filter ball tracklets BEFORE the loop
        ball_tracklets = [t for t in tracklets if is_ball_tracklet(t.name, self.pre_results)]

        # ✅ Filter GT=-1 tracklets BEFORE the loop
        gt_illegible = [t for t in tracklets if self.ground_truth.get(t.name, '-1') == '-1' and not is_ball_tracklet(t.name, self.pre_results)]

        # ✅ Only process tracklets that are NOT ball AND NOT GT=-1
        processable = [t for t in tracklets
                       if not is_ball_tracklet(t.name, self.pre_results)
                       and self.ground_truth.get(t.name, '-1') != '-1']

        if self.config.get('verbose', False):
            print(f"📦 Processing {len(processable)} tracklets")
            print(f"   ⚽ Ball tracklets skipped: {len(ball_tracklets)}")
            print(f"   ❌ GT=-1 tracklets skipped: {len(gt_illegible)}")

        results = []
        stats = {'illegible': 0, 'total_legible_images': 0, 'total_processed': 0, 'gt_illegible': len(gt_illegible)}

        # ✅ tqdm bar ONLY iterates over processable tracklets
        for tracklet in tqdm(processable, desc="Legibility", disable=False):
            result = score_tracklet_legibility(str(tracklet), tracklet.name, self.outlier_data)
            result['ground_truth'] = self.ground_truth.get(tracklet.name, '-1')
            results.append(result)
            self.processed_data[tracklet.name] = result

            if result['status'] == 'success' and result.get('legible_images'):
                stats['total_processed'] += 1
                stats['total_legible_images'] += len(result['legible_images'])
            if result.get('marked_illegible'):
                stats['illegible'] += 1

        # ✅ Add ball tracklets to results for complete report
        for t in ball_tracklets:
            ball_result = {
                'tracklet_id': t.name,
                'tracklet_number': extract_tracklet_number(t.name),
                'status': 'skipped_ball_tracklet',
                'is_ball_tracklet': True,
                'ground_truth': self.ground_truth.get(t.name, '-1'),
                'legible_images': [],
                'legible_count': 0
            }
            results.append(ball_result)
            self.processed_data[t.name] = ball_result

        # ✅ Add GT=-1 tracklets to results for complete report
        for t in gt_illegible:
            gt_result = {
                'tracklet_id': t.name,
                'tracklet_number': extract_tracklet_number(t.name),
                'status': 'skipped_gt_illegible',
                'is_ball_tracklet': False,
                'ground_truth': self.ground_truth.get(t.name, '-1'),
                'legible_images': [],
                'legible_count': 0
            }
            results.append(gt_result)
            self.processed_data[t.name] = gt_result

        # Sort results by tracklet number for consistent output
        results.sort(key=lambda x: x.get('tracklet_number', float('inf')))

        # Save report
        report = {
            'timestamp': datetime.now().isoformat(),
            'config': {k: v for k, v in self.config.items() if k != 'backend'},
            'summary': {
                'total_tracklets': len(tracklets),
                'ball_tracklets_skipped': len(ball_tracklets),
                'gt_illegible_skipped': stats['gt_illegible'],
                'illegible_tracklets': stats['illegible'],
                'processed_tracklets': stats['total_processed'],
                'total_legible_images': stats['total_legible_images'],
            },
            'tracklets': {
                r['tracklet_id']: {
                    'status': r['status'],
                    'legible_count': r.get('legible_count', 0),
                    'legible_images': r.get('legible_images', []),
                    'ground_truth': r.get('ground_truth', '-1'),
                    'is_ball_tracklet': r.get('is_ball_tracklet', False)
                } for r in results
            }
        }

        report_path = Path(self.config['output_folder']) / 'legibility_report.json'
        with open(report_path, 'w') as f:
            json.dump(report, f, indent=2, default=str)

        print(f"✅ Done | Processed: {stats['total_processed']} | Legible: {stats['total_legible_images']} | Skipped (balls): {len(ball_tracklets)} | Skipped (GT=-1): {stats['gt_illegible']}")
        print(f"📄 Saved: {report_path}")

        return self.processed_data

