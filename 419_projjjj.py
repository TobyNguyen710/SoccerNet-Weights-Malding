class LegibilityClassifier34(nn.Module):
    def __init__(self, num_classes: int = 1, pretrained: bool = True):
        super().__init__()
        self.backbone = models.resnet34(weights=models.ResNet34_Weights.IMAGENET1K_V1 if pretrained else None)
        self.backbone.fc = nn.Linear(self.backbone.fc.in_features, num_classes)
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return torch.sigmoid(self.backbone(x))

class MLLegibilityClassifier:
    def __init__(self, model_path: str, device: str = 'cpu', threshold: float = 0.5, verbose: bool = True):
        self.device = device
        self.threshold = threshold
        self.verbose = verbose
        checkpoint_info = self._inspect_checkpoint(model_path)
        self.model = self._load_model(model_path, checkpoint_info).to(device).eval()
        self.transform = transforms.Compose([
            transforms.Resize((Config.IMAGE_SIZE, Config.IMAGE_SIZE)),
            transforms.ToTensor(),
            transforms.Normalize(mean=Config.IMAGENET_MEAN, std=Config.IMAGENET_STD)
        ])
        if verbose:
            print(f"🎯 ML Backend Ready (ResNet34) on {device}")

    def _inspect_checkpoint(self, model_path: str) -> Dict[str, Any]:
        try:
            checkpoint = torch.load(model_path, map_location='cpu', weights_only=False)
            state_dict = checkpoint.get('model_state_dict', checkpoint.get('state_dict', checkpoint))
            keys = list(state_dict.keys())
            prefixes = set(k.split('.')[0] for k in keys if '.' in k)
            return {'state_dict': state_dict, 'total_keys': len(keys), 'prefixes': list(prefixes), 'sample_keys': keys[:5]}
        except Exception as e:
            return {'state_dict': None, 'total_keys': 0, 'prefixes': [], 'sample_keys': [], 'error': str(e)}

    def _load_model(self, model_path: str, checkpoint_info: Dict[str, Any]) -> nn.Module:
        try:
            model = LegibilityClassifier34(pretrained=True)
            model_state = model.state_dict()
            state_dict = checkpoint_info['state_dict']
            new_state_dict = {}
            for key, value in state_dict.items():
                for possible_key in [key, key.replace('model_ft.', 'backbone.'), key.replace('model.', 'backbone.'),
                                    f'backbone.{key}' if not any(key.startswith(p) for p in ['model_ft.', 'model.', 'backbone.']) else key]:
                    if possible_key in model_state:
                        new_state_dict[possible_key] = value
                        break
            model.load_state_dict(new_state_dict, strict=False)
            return model
        except Exception as e:
            if self.verbose:
                print(f"⚠ Error loading model: {e}, falling back to CPU")
            self.device = 'cpu'
            model = LegibilityClassifier34(pretrained=True)
            if checkpoint_info['state_dict']:
                model.load_state_dict(checkpoint_info['state_dict'], strict=False)
            return model

    def classify(self, img_path: Union[str, Path]) -> Dict[str, Any]:
        try:
            img_tensor = self.transform(Image.open(str(img_path)).convert('RGB')).unsqueeze(0).to(self.device)
            with torch.no_grad():
                score = float(self.model(img_tensor).item())
            legible = bool(score >= self.threshold)
            return {'image': str(img_path), 'legible': legible,
                   'status': (LegibilityStatus.LEGIBLE if legible else LegibilityStatus.UNKNOWN).value,
                   'score': score, 'metrics': {'model_score': score}}
        except Exception as e:
            return {'image': str(img_path), 'legible': False, 'status': LegibilityStatus.UNKNOWN.value,
                   'score': 0.0, 'metrics': {}, 'error': str(e)}

