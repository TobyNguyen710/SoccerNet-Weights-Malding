class LLMInferencePipeline:
    def __init__(self, config: dict):
        self.config = config
        self.pre_results = {}
        self.results = []

    def initialize(self):
        print(f"\n{'='*70}")
        print(f"🤖 CELL 2: LLM INFERENCE & PRE-RESULTS UPDATE")
        print(f"{'='*70}")
        print(f"Output:        {self.config['output_folder']}")
        print(f"Pre-Results:   {self.config['pre_results_path']}")
        print(f"Groq Model:    {self.config['groq_model']}")
        print(f"Cropped Folder: {self.config['temp_cropped_folder']}")
        print(f"{'='*70}\n")

        # Load pre_results
        print(f"📖 Loading pre_results.json...")
        self.pre_results = load_pre_results(self.config['pre_results_path'])
        print(f"   ✓ Loaded {len(self.pre_results)} tracklets")

        ball_count = sum(1 for v in self.pre_results.values() if v == 1)
        illegible_count = sum(1 for v in self.pre_results.values() if v == -1)
        print(f"   ⚽ Ball tracklets: {ball_count}")
        print(f"   ⚠️  Illegible tracklets: {illegible_count}")
        print(f"\n{'='*70}\n")

        return True

    def run_inference(self):
        """Run LLM inference on all tracklet folders in temp_cropped"""
        if not self.initialize():
            return None

        # Get tracklet folders from temp_cropped
        cropped_path = Path(self.config['temp_cropped_folder'])
        tracklet_folders = [d for d in cropped_path.iterdir() if d.is_dir()]
        tracklet_folders = sorted(tracklet_folders, key=lambda x: int(x.name) if x.name.isdigit() else 0)

        print(f"📦 Found {len(tracklet_folders)} tracklet folders in temp_cropped/")
        print(f"🤖 Running LLM inference...\n")

        for tracklet_folder in tqdm(tracklet_folders, desc="LLM Inference"):
            tracklet_id = tracklet_folder.name

            result = {
                'tracklet_id': tracklet_id,
                'prediction': '-1',
                'confidence': 0.0,
                'llm_responses': [],
                'llm_calls': 0,
                'status': 'success',
                'error': None,
                'images_processed': 0
            }

            if self.config['verbose']:
                print(f"\n{'─'*50}")
                print(f"TRACKLET {tracklet_id}")
                print(f"{'─'*50}")

            # Check if already in pre_results
            if not should_process_tracklet(tracklet_id, self.pre_results):
                result['status'] = 'skipped'
                self.results.append(result)
                continue

            try:
                # Get all cropped images for this tracklet
                cropped_images = sorted(tracklet_folder.glob('*_torso_crop.png'))

                if not cropped_images:
                    # Also check for other image formats
                    cropped_images = sorted(list(tracklet_folder.glob('*.png')) +
                                           list(tracklet_folder.glob('*.jpg')))

                if not cropped_images:
                    result['status'] = 'no_images'
                    result['prediction'] = '-1'
                    self.results.append(result)
                    # Mark as illegible in pre_results
                    if self.config['update_pre_results']:
                        self.pre_results[tracklet_id] = -1
                        save_pre_results(self.config['pre_results_path'], self.pre_results)
                    if self.config['verbose']:
                        print(f"   ⚠️  No cropped images found - marked as illegible (-1)")
                    continue

                result['images_processed'] = len(cropped_images)
                predictions = []

                for img_path in cropped_images:
                    if self.config['verbose']:
                        print(f"   🤖 Calling LLM: {img_path.name}")

                    number = predict_jersey_number(
                        str(img_path),
                        model=self.config['groq_model']
                    )

                    if self.config['verbose']:
                        print(f"      → Prediction: {number}")

                    predictions.append(number)
                    result['llm_responses'].append(number)
                    result['llm_calls'] += 1

                # Consensus voting - finalize prediction
                if predictions:
                    counter = Counter(predictions)
                    most_common = counter.most_common(1)[0]
                    result['prediction'] = most_common[0]
                    result['confidence'] = most_common[1] / len(predictions)
                    result['vote_distribution'] = dict(counter)

                    if self.config['verbose']:
                        print(f"   📊 Vote distribution: {dict(counter)}")
                        print(f"   🎯 Final prediction: {result['prediction']} (confidence: {result['confidence']*100:.1f}%)")

                    # Update pre_results with prediction
                    if self.config['update_pre_results'] and result['prediction'] != '-1':
                        # Only update if prediction is a valid number
                        try:
                            pred_num = int(result['prediction'])
                            if 0 < pred_num < 100:
                                self.pre_results[tracklet_id] = pred_num
                                if self.config['verbose']:
                                    print(f"   ✓ Updated pre_results: {tracklet_id} → {pred_num}")
                            else:
                                self.pre_results[tracklet_id] = -1
                                if self.config['verbose']:
                                    print(f"   ⚠️  Invalid prediction ({pred_num}) - marked as -1")
                        except ValueError:
                            self.pre_results[tracklet_id] = -1
                            if self.config['verbose']:
                                print(f"   ⚠️  Non-numeric prediction - marked as -1")

            except Exception as e:
                result['status'] = 'error'
                result['error'] = str(e)
                result['prediction'] = '-1'
                if self.config['update_pre_results']:
                    self.pre_results[tracklet_id] = -1

            self.results.append(result)

            if self.config['verbose']:
                status_icon = "✅" if result['prediction'] != '-1' else "❌"
                print(f"   {status_icon} Tracklet {tracklet_id}: {result['prediction']}")

        # Save final pre_results
        if self.config['update_pre_results']:
            print(f"\n{'='*70}")
            print(f"💾 SAVING UPDATED pre_results.json")
            print(f"{'='*70}")
            save_pre_results(self.config['pre_results_path'], self.pre_results)

        return self.results

    def print_summary(self):
        """Print inference summary"""
        print(f"\n{'='*70}")
        print(f"📊 INFERENCE SUMMARY")
        print(f"{'='*70}")

        total = len(self.results)
