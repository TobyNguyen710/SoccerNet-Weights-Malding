def get_legible_sequences(tracklet_dir: str, pipeline, images_to_keep: List[str],
                          top_k_sequences: int = 2, min_score: float = 0.5) -> Tuple[List[str], List[Dict]]:
    """
    Get top-K sequences of legible images.
    Each sequence consists of 5 frames: [center-2, center-1, center, center+1, center+2].
    Returns: (list_of_unique_image_paths, list_of_sequence_details)
    """
    # 1. Gather all valid image paths present in both disk and keep_list
    valid_image_paths = []
    for ext in ['*.jpg', '*.png']:
        for f in Path(tracklet_dir).glob(ext):
            frame_id = f.stem
            if is_image_in_keep_list(frame_id, images_to_keep):
                valid_image_paths.append(str(f))

    # 2. Sort frames numerically to ensure neighbors are actually neighbors
    valid_image_paths = sort_frames_numerically(list(set(valid_image_paths)))

    if len(valid_image_paths) < CONFIG['sequence_window']:
        return [], []

    # 3. Classify ALL valid images once (cache scores)
    score_cache = {}
    for img_path in valid_image_paths:
        result = pipeline.classify_image(img_path)
        score_cache[img_path] = result['score']

    # 🆕 DEBUG: Show individual frame scores
    if CONFIG['verbose']:
        print(f"   🔍 Individual frame scores (top {CONFIG['top_k_legible']} shown):")
        sorted_scores = sorted(score_cache.items(), key=lambda x: x[1], reverse=True)
        for i, (path, score) in enumerate(sorted_scores[:CONFIG['top_k_legible']]):
            frame_num = extract_frame_number(path)
            print(f"      • Frame {frame_num:3d}: {score*100:5.1f}% legible  ← {Path(path).name}")

    # 4. Identify Valid Sequences
    candidates = []
    half_window = CONFIG['sequence_window'] // 2

    for i in range(half_window, len(valid_image_paths) - half_window):
        center_path = valid_image_paths[i]
        center_score = score_cache[center_path]

        if center_score >= min_score:
            window_paths = valid_image_paths[i-half_window : i+half_window+1]
            window_scores = [score_cache[p] for p in window_paths]
            mean_score = np.mean(window_scores)

            candidates.append({
                'center_frame': center_path,
                'center_score': center_score,
                'mean_score': mean_score,
                'window_paths': window_paths,
                'window_scores': window_scores
            })

    if not candidates:
        if CONFIG['verbose']:
            print(f"   ⚠️  No valid sequences found (no center frame ≥ {min_score*100:.0f}% with 2 neighbors)\n")
        return [], []

    # 🆕 DEBUG: Show all candidate sequences
    if CONFIG['verbose'] and candidates:
        print(f"   🔍 Candidate sequences (center frame ≥ {CONFIG['legibility_threshold']*100:.0f}%):")
        for i, cand in enumerate(sorted(candidates, key=lambda x: x['mean_score'], reverse=True)[:10]):
            frames = [extract_frame_number(p) for p in cand['window_paths']]
            scores = [f"{s*100:.0f}%" for s in cand['window_scores']]
            print(f"      #{i+1}: frames {frames} → scores [{', '.join(scores)}] → avg={cand['mean_score']*100:.1f}%")
        if len(candidates) > 10:
            print(f"      ... and {len(candidates)-10} more candidates")

    # 5. Select Top-K Sequences based on Mean Score
    candidates.sort(key=lambda x: x['mean_score'], reverse=True)
    top_sequences = candidates[:top_k_sequences]

    # 🆕 DEBUG: Show final selected sequences
    if CONFIG['verbose'] and top_sequences:
        print(f"   ✅ Top {len(top_sequences)} sequence(s) selected:")
        for i, seq in enumerate(top_sequences, 1):
            frames = [extract_frame_number(p) for p in seq['window_paths']]
            scores = [f"{s*100:.1f}%" for s in seq['window_scores']]
            print(f"      🎯 Seq #{i}: frames {frames}")
            print(f"                scores: [{', '.join(scores)}]")
            print(f"                → mean: {seq['mean_score']*100:.1f}%")

    # 6. Flatten paths (keep unique)
    final_paths = []
    seen = set()
    for seq in top_sequences:
        for p in seq['window_paths']:
            if p not in seen:
                final_paths.append(p)
                seen.add(p)

    final_paths = sort_frames_numerically(final_paths)

    # 🆕 DEBUG: Show final unique output
    if CONFIG['verbose']:
        final_frames = [extract_frame_number(p) for p in final_paths]
        print(f"   📦 Final unique frames to process: {final_frames} (count={len(final_frames)})\n")

    return final_paths, top_sequences

def score_tracklet_legibility(tracklet_dir: str, tracklet_name: str, outlier_data: dict) -> dict:
    """Score tracklet using Sequence-Based Legibility"""
    global PRE_RESULTS

    result = {
        'tracklet_id': tracklet_name,
        'tracklet_number': extract_tracklet_number(tracklet_name),
        'total_frames': 0,
        'frames_in_keep_list': 0,
        'sequences_found': 0,
        'legible_images': [],
        'sequence_details': [], # 🆕 Store sequence info
        'status': 'success',
        'is_ball_tracklet': False,
        'marked_illegible': False,
        'images_to_keep_count': 0,
    }

    try:
        # Check if this is a ball tracklet
        if is_ball_tracklet(tracklet_name, PRE_RESULTS):
            result['status'] = 'skipped_ball_tracklet'
            result['is_ball_tracklet'] = True
            if CONFIG['verbose']:
                print(f"   ⚽ Skipping tracklet {tracklet_name} (marked as ball)")
            return result

        # Get images_to_keep from outlier_elimination.json
        images_to_keep = get_images_to_keep(tracklet_name, outlier_data)
        result['images_to_keep_count'] = len(images_to_keep)

        # Count total frames on disk
        image_paths = sorted([str(f) for f in Path(tracklet_dir).glob('*.jpg')])
        result['total_frames'] = len(image_paths)

        # Count frames in keep list
        filtered_count = 0
        for img_path in image_paths:
            frame_id = Path(img_path).stem
            if is_image_in_keep_list(frame_id, images_to_keep):
                filtered_count += 1
        result['frames_in_keep_list'] = filtered_count

        if filtered_count < CONFIG['sequence_window']:
            result['status'] = 'insufficient_frames_for_sequence'
            PRE_RESULTS = mark_tracklet_illegible(tracklet_name, PRE_RESULTS, CONFIG['pre_results_path'])
            result['marked_illegible'] = True
            if CONFIG['verbose']:
                print(f"   ⚠️  Insufficient frames ({filtered_count}) for sequence window ({CONFIG['sequence_window']})")
            return result

        # Load legibility pipeline
        legibility_pipeline = load_legibility_pipeline()

        # 🆕 Get legible sequences
        legible_images, sequence_details = get_legible_sequences(
            tracklet_dir, legibility_pipeline, images_to_keep,
            top_k_sequences=CONFIG['top_k_sequences'],
            min_score=CONFIG['legibility_threshold']
        )

        result['sequences_found'] = len(sequence_details)
        result['legible_images'] = legible_images
        result['sequence_details'] = sequence_details # Save details for report

        if CONFIG['verbose']:
            print(f"   📊 Found {len(sequence_details)} sequences ({len(legible_images)} unique images)")

        if not legible_images:
            result['status'] = 'no_legible_sequences'
            PRE_RESULTS = mark_tracklet_illegible(tracklet_name, PRE_RESULTS, CONFIG['pre_results_path'])
            result['marked_illegible'] = True
            if CONFIG['verbose']:
                print(f"   ⚠️  No legible sequences found for tracklet {tracklet_name}")
            return result

        return result

    except Exception as e:
        result['status'] = 'error'
        result['error'] = str(e)
        PRE_RESULTS = mark_tracklet_illegible(tracklet_name, PRE_RESULTS, CONFIG['pre_results_path'])
        result['marked_illegible'] = True
        return result

