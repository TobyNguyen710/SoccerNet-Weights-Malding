def load_json(path: str) -> dict:
    """Load JSON file safely"""
    with open(path, 'r') as f:
        return json.load(f)

def normalize_tracklet_id(tracklet_id: str) -> str:
    """Remove 'test_' prefix if present for consistent matching"""
    if tracklet_id.startswith('test_'):
        return tracklet_id.replace('test_', '', 1)
    return tracklet_id

def is_valid_jersey_number(value) -> bool:
    """Check if value is a valid jersey number (2-99)"""
    try:
        num = int(value)
        return 2 <= num <= 99
    except (ValueError, TypeError):
        return False

def is_ball_prediction(value, ball_value=0) -> bool:
    """Check if prediction is a ball detection (value = 0)"""
    try:
        return int(value) == ball_value
    except (ValueError, TypeError):
        return False

def is_ball_ground_truth(value) -> bool:
    """Check if ground truth indicates a ball (value = 1 in GT)"""
    try:
        return int(value) == 1
    except (ValueError, TypeError):
        return False

def evaluate_predictions(pre_results: dict, ground_truth: dict, ball_value: int = 0, exclude_balls: bool = True) -> dict:
    """
    Compare predictions against ground truth and compute metrics.

    KEY RULES:
    - Main accuracy: COMPLETELY IGNORE predictions where value=0 (balls)
    - Accuracy with balls: Count ALL ball tracklets from GT, assume they're all correct
    """
    metrics = {
        'total_tracklets': 0,
        'evaluated_tracklets': 0,  # Excludes balls from main accuracy
        'ball_tracklets': 0,       # Tracklets that are balls in GT

        # Classification metrics (main accuracy - excludes balls COMPLETELY)
        'correct': 0,
        'incorrect': 0,
        'illegible_correct': 0,   # GT=-1, Pred=-1
        'illegible_incorrect': 0, # GT=-1, Pred!= -1 (FP)
        'legible_missed': 0,      # GT!= -1, Pred=-1 (FN)

        # Ball-specific metrics (tracked separately, NOT in main accuracy)
        'ball_predictions': 0,    # Count of predictions where value=0
        'ball_correct': 0,        # GT=1 (ball), Pred=0 (ball detected)
        'ball_incorrect': 0,      # GT!=1, Pred=0 (false ball detection)
        'ball_missed': 0,         # GT=1, Pred!=0 (missed ball)

        # Per-number stats
        'by_number': defaultdict(lambda: {'tp': 0, 'fp': 0, 'fn': 0, 'total_gt': 0}),

        # Detailed results
        'results': [],
        'mistakes': [],
        'confusion_matrix': defaultdict(lambda: defaultdict(int)),

        # Hypothetical metrics
        'accuracy_with_balls': 0  # Accuracy assuming ALL ball predictions correct
    }

    # Process each prediction
    for pred_id, pred_value in pre_results.items():
        norm_id = normalize_tracklet_id(pred_id)

        # Skip if ground truth doesn't exist
        if norm_id not in ground_truth:
            continue

        gt_value = ground_truth[norm_id]
        metrics['total_tracklets'] += 1

        # Check if this is a ball tracklet in ground truth
        is_gt_ball = is_ball_ground_truth(gt_value)
        is_pred_ball = is_ball_prediction(pred_value, ball_value)

        if is_gt_ball:
            metrics['ball_tracklets'] += 1

        # Track ball predictions (pred_value = 0)
        if is_pred_ball:
            metrics['ball_predictions'] += 1
            if is_gt_ball:
                metrics['ball_correct'] += 1
            else:
                metrics['ball_incorrect'] += 1
        elif is_gt_ball:
            metrics['ball_missed'] += 1

        # ⭐ MAIN ACCURACY: COMPLETELY SKIP predictions where value=0 (balls)
        if exclude_balls and is_pred_ball:
            # Do NOT count ball predictions in main accuracy at all
            continue

        metrics['evaluated_tracklets'] += 1

        # Convert to comparable format
        try:
            pred_num = int(pred_value)
        except (ValueError, TypeError):
            pred_num = pred_value

        try:
            gt_num = int(gt_value)
        except (ValueError, TypeError):
            gt_num = gt_value

        # Track per-number ground truth counts
        if is_valid_jersey_number(gt_value):
            metrics['by_number'][gt_num]['total_gt'] += 1

        # Evaluate
        result = {
            'tracklet_id': pred_id,
            'normalized_id': norm_id,
            'prediction': pred_value,
            'ground_truth': gt_value,
            'correct': False,
            'is_ball_gt': is_gt_ball,
            'is_ball_pred': is_pred_ball
        }

        # Case 1: Exact match (including illegible)
        if str(pred_value) == str(gt_value):
            metrics['correct'] += 1
            result['correct'] = True

            if gt_value == -1 or gt_value == '-1':
                metrics['illegible_correct'] += 1
                metrics['confusion_matrix'][-1][-1] += 1
            elif is_valid_jersey_number(gt_value):
                metrics['by_number'][gt_num]['tp'] += 1
                metrics['confusion_matrix'][gt_num][gt_num] += 1

        # Case 2: Illegible errors
        elif pred_value == -1 or pred_value == '-1':
            if gt_value != -1 and gt_value != '-1':
                # Missed a legible jersey (False Negative)
                metrics['incorrect'] += 1
                metrics['legible_missed'] += 1
                if is_valid_jersey_number(gt_value):
                    metrics['by_number'][gt_num]['fn'] += 1
                    metrics['confusion_matrix'][gt_num][-1] += 1
                result['error_type'] = 'false_negative'
                metrics['mistakes'].append(result)

        # Case 3: Predicted number for illegible tracklet (False Positive)
        elif (gt_value == -1 or gt_value == '-1') and (pred_value != -1 and pred_value != '-1'):
            metrics['incorrect'] += 1
            metrics['illegible_incorrect'] += 1
            if is_valid_jersey_number(pred_value):
                pred_num_check = int(pred_value)
                metrics['by_number'][pred_num_check]['fp'] += 1
                metrics['confusion_matrix'][-1][pred_num_check] += 1
            result['error_type'] = 'false_positive'
            metrics['mistakes'].append(result)

        # Case 4: Wrong number predicted
        else:
            metrics['incorrect'] += 1
            if is_valid_jersey_number(gt_value) and is_valid_jersey_number(pred_value):
                metrics['by_number'][gt_num]['fn'] += 1
                metrics['by_number'][pred_num]['fp'] += 1
                metrics['confusion_matrix'][gt_num][pred_num] += 1
            result['error_type'] = 'wrong_number'
            metrics['mistakes'].append(result)

        metrics['results'].append(result)

    # Calculate main accuracy (jersey + illegible ONLY, predictions with value=0 excluded)
    if metrics['evaluated_tracklets'] > 0:
        metrics['accuracy'] = metrics['correct'] / metrics['evaluated_tracklets'] * 100
    else:
        metrics['accuracy'] = 0

    # ⭐ Calculate "accuracy with balls" - assumes ALL ball tracklets from GT are correct
    # Formula: (correct_jersey_illegible + ALL_ball_tracklets_from_GT) / (evaluated_tracklets + ALL_ball_tracklets_from_GT)
    hypothetical_correct = metrics['correct'] + metrics['ball_tracklets']  # ALL balls from GT assumed correct
    hypothetical_total = metrics['evaluated_tracklets'] + metrics['ball_tracklets']
    if hypothetical_total > 0:
        metrics['accuracy_with_balls'] = hypothetical_correct / hypothetical_total * 100
    else:
        metrics['accuracy_with_balls'] = 0

    # Precision/Recall/F1 for jersey number classification (excluding -1 and balls)
    total_tp = sum(d['tp'] for d in metrics['by_number'].values())
    total_fp = sum(d['fp'] for d in metrics['by_number'].values())
    total_fn = sum(d['fn'] for d in metrics['by_number'].values())

    metrics['precision'] = total_tp / (total_tp + total_fp) * 100 if (total_tp + total_fp) > 0 else 0
    metrics['recall'] = total_tp / (total_tp + total_fn) * 100 if (total_tp + total_fn) > 0 else 0
    metrics['f1'] = (2 * metrics['precision'] * metrics['recall'] /
                    (metrics['precision'] + metrics['recall']) if
                    (metrics['precision'] + metrics['recall']) > 0 else 0)

    # Convert defaultdicts to regular dicts for JSON serialization
    metrics['by_number'] = {int(k): v for k, v in sorted(metrics['by_number'].items())}
    metrics['confusion_matrix'] = {str(int(k)) if isinstance(k, int) and k != -999 else str(k):
                                   {str(int(vk)) if isinstance(vk, int) and vk != -999 else str(vk): vv
                                    for vk, vv in v.items()}
                                   for k, v in metrics['confusion_matrix'].items()}

    return metrics

