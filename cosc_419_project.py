            if ocr_number != "-1":
                votes.append((ocr_number, 1.0))  # OCR normal weight

            if votes:
                # Pick the number with the highest weight
                number, conf_score = max(votes, key=lambda x: x[1])
                source = "PARSeq" if number == parseq_number else "OCR"
