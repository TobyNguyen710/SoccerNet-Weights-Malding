    if not image_paths:
        return []

    scored = []
    for img_path in image_paths:
        result = pipeline.classify_image(img_path)
        if result['legible'] and result['score'] >= min_score:
            scored.append((img_path, result['score']))

    scored.sort(key=lambda x: x[1], reverse=True)
    return [path for path, score in scored[:top_k]]

