class LegibilityClassifierPipeline:
    def __init__(self, backend: str = 'auto', model_path: Optional[str] = None,
                 device: Optional[str] = None, threshold: float = 0.5, verbose: bool = True, strict_mode: bool = False):
        self.verbose = verbose
        self.threshold = threshold
        self.strict_mode = strict_mode
        self.device = device or get_device(verbose)
        if backend == 'auto':
            backend = 'ml' if model_path and Path(model_path).exists() else 'heuristic'
        if backend == 'ml' and model_path and Path(model_path).exists():
            try:
                self.classifier = MLLegibilityClassifier(model_path, self.device, threshold, verbose)
                self.backend = 'ml'
            except Exception as e:
                if verbose:
                    print(f"⚠ ML backend failed: {e}, falling back to heuristic")
                self.classifier = HeuristicLegibilityClassifier(verbose=verbose)
                self.backend = 'heuristic'
        else:
            params = {'blur_threshold': 200.0, 'min_brightness': 50.0, 'max_brightness': 200.0,
                     'min_contrast': 40.0, 'min_score_threshold': 0.55} if strict_mode else {}
            self.classifier = HeuristicLegibilityClassifier(verbose=verbose, **params)
            self.backend = 'heuristic'
        if verbose:
            print(f"🔧 Legibility Classifier Ready (Backend: {self.backend}, Device: {self.device})")

    def classify_image(self, img_path: Union[str, Path]) -> Dict[str, Any]:
        return self.classifier.classify(img_path)

    def process_tracklet(self, tracklet_dir: Union[str, Path], filter_legible: bool = True) -> Tuple[List[Dict], List[str]]:
        tracklet_path = Path(tracklet_dir)
        if not tracklet_path.is_dir():
            raise NotADirectoryError(f"Tracklet directory not found: {tracklet_path}")
        image_paths = sorted([f for f in tracklet_path.iterdir()
                             if f.suffix.lower() in {'.jpg', '.jpeg', '.png', '.bmp'} and f.is_file()])
        results, kept = [], []
        for img_path in tqdm(image_paths, desc=f"Legibility: {tracklet_path.name}", disable=not self.verbose):
            result = self.classify_image(img_path)
            results.append(result)
            if not filter_legible or result['legible']:
                kept.append(str(img_path))
        return results, kept

    def process_dataset(self, tracklets_folder: Union[str, Path], output_folder: Optional[Union[str, Path]] = None,
                       save_results: bool = True, filter_legible: bool = True, max_tracklets: Optional[int] = None) -> Dict[str, Any]:
        tracklets_path = Path(tracklets_folder)
        if not tracklets_path.is_dir():
            raise NotADirectoryError(f"Tracklets folder not found: {tracklets_path}")
        tracklets = sorted([d for d in tracklets_path.iterdir() if d.is_dir() and not d.name.startswith('.')])
        if not tracklets:
            return {"processed": 0, "total_frames": 0, "legible_frames": 0}
        if max_tracklets:
            tracklets = tracklets[:max_tracklets]
        output_path = Path(output_folder) if output_folder and save_results else None
        if output_path:
            output_path.mkdir(parents=True, exist_ok=True)
        stats = {"processed": 0, "total_frames": 0, "legible_frames": 0, "filtered_frames": 0,
                "failed_tracklets": 0, "status_counts": defaultdict(int), "tracklet_stats": {},
                "backend": self.backend, "device": self.device, "threshold": self.threshold,
                "filter_legible": filter_legible, "strict_mode": self.strict_mode,
                "timestamp": datetime.now().isoformat()}
        for tracklet in tqdm(tracklets, desc="Processing tracklets", disable=not self.verbose):
            try:
                results, kept = self.process_tracklet(tracklet, filter_legible=filter_legible)
                if not results:
                    stats["failed_tracklets"] += 1
                    continue
                for result in results:
                    stats["status_counts"][result['status']] += 1
                    if result['legible']:
                        stats["legible_frames"] += 1
                stats["total_frames"] += len(results)
                stats["filtered_frames"] += len(kept)
                if output_path and save_results:
                    with open(output_path / f"{tracklet.name}_legibility.json", 'w') as f:
                        json.dump(convert_to_serializable(results), f, indent=2)
                    if filter_legible:
                        with open(output_path / f"{tracklet.name}_kept_images.txt", 'w') as f:
                            f.write('\n'.join(kept))
                stats["processed"] += 1
                stats["tracklet_stats"][tracklet.name] = {
                    "total_frames": len(results), "legible_frames": len(kept),
                    "legibility_ratio": len(kept) / len(results) if results else 0
                }
            except Exception as e:
                print(f"✗ Error processing {tracklet.name}: {e}")
                stats["failed_tracklets"] += 1
        stats["status_counts"] = dict(stats["status_counts"])
        if self.verbose and stats["total_frames"] > 0:
            legible_pct = stats['legible_frames'] / stats['total_frames'] * 100
            print(f"\n✅ Complete: {stats['legible_frames']}/{stats['total_frames']} legible ({legible_pct:.1f}%)")
        return stats

    def cleanup(self):
        if self.verbose:
            print("🧹 Cleaned up")

