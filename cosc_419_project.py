class EasyOCRProcessor:
    """Improved EasyOCR cropper with multiple detection strategies for Colab"""

    def __init__(self, pose_folder: str = "out/pose/train", use_gpu: bool = False):
        print("📖 Initializing EasyOCR...")
        self.reader = easyocr.Reader(['en'], gpu=use_gpu, verbose=False)
        self.pose_folder = Path(pose_folder)
        print(f"✓ EasyOCR ready (GPU: {use_gpu})")

    def _get_pose_region(self, image_path: str, tracklet_name: str) -> tuple:
        """Get approximate jersey region from pose keypoints"""
        try:
            pose_file = self.pose_folder / f"{tracklet_name}_pose.json"
            if not pose_file.exists():
                return None
            with open(pose_file, 'r') as f:
                pose_data = json.load(f)
            img_name = Path(image_path).name
            for item in pose_data:
                if Path(item['image']).name == img_name:
                    if not item.get('pose_detected', False):
                        return None
                    keypoints = item.get('keypoints', {})
                    ls, rs, lh, rh = [keypoints.get(k, {}) for k in ['left_shoulder', 'right_shoulder', 'left_hip', 'right_hip']]
                    if not all(k and k.get('x') for k in [ls, rs, lh, rh]):
                        return None
                    x1 = min(ls['x'], rs['x']); x2 = max(ls['x'], rs['x'])
                    y1 = min(ls['y'], rs['y']); y2 = (ls['y'] + rs['y'] + lh['y'] + rh['y']) / 4
                    width, padding_x = x2 - x1, int((x2 - x1) * 0.3)
                    padding_y = int((y2 - y1) * 0.2)
                    return (max(0, int(x1) - padding_x), max(0, int(y1) - padding_y),
                           int(x2) + padding_x, int(y2) + padding_y)
            return None
        except Exception as e:
            return None

    def _crop_from_pose(self, image: np.ndarray, pose_region: tuple) -> np.ndarray:
        x1, y1, x2, y2 = pose_region
        return image[y1:y2, x1:x2]

    def _preprocess_multiple(self, image: np.ndarray) -> list:
        versions = [('original', image)]
        gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
        versions.append(('gray', cv2.cvtColor(gray, cv2.COLOR_GRAY2BGR)))
        clahe = cv2.createCLAHE(clipLimit=3.0, tileGridSize=(8,8))
        versions.append(('clahe', cv2.cvtColor(clahe.apply(gray), cv2.COLOR_GRAY2BGR)))
        _, binary = cv2.threshold(gray, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
        versions.append(('binary', cv2.cvtColor(binary, cv2.COLOR_GRAY2BGR)))
        return versions

    def _detect_with_fallback(self, image: np.ndarray) -> list:
        best_results = []
        for name, img in self._preprocess_multiple(image):
            for scale in [1.0, 0.75, 0.5]:
                scaled = cv2.resize(img, (int(img.shape[1]*scale), int(img.shape[0]*scale))) if scale < 1.0 else img
                for threshold in [0.3, 0.2, 0.1]:
                    results = self.reader.readtext(scaled, paragraph=False, text_threshold=threshold, low_text=0.2, width_ths=0.7, height_ths=0.7)
                    if results:
                        if scale < 1.0:
                            scaled_results = [(np.array(bbox) / scale, text, conf) for bbox, text, conf in results]
                            best_results.extend(scaled_results)
                        else:
                            best_results.extend(results)
                        if len(best_results) > 5:
                            return best_results
        return best_results

    def _get_tight_bounds(self, all_boxes: np.ndarray, img_shape: tuple) -> tuple:
        if len(all_boxes) == 0:
            return None
        h, w = img_shape[:2]
        x_min, y_min = np.min(all_boxes[:, 0]), np.min(all_boxes[:, 1])
        x_max, y_max = np.max(all_boxes[:, 2]), np.max(all_boxes[:, 3])
        text_width, text_height = x_max - x_min, y_max - y_min
        pad_x, pad_y = max(5, min(20, int(text_width * 0.1))), max(5, min(20, int(text_height * 0.1)))
        return (max(0, int(x_min) - pad_x), max(0, int(y_min) - pad_y),
                min(w, int(x_max) + pad_x), min(h, int(y_max) + pad_y))

    def _heuristic_crop(self, image: np.ndarray) -> tuple:
        gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
        all_regions = []
        edges = cv2.Canny(gray, 30, 100)
        kernel = cv2.getStructuringElement(cv2.MORPH_RECT, (3,3))
        dilated = cv2.dilate(edges, kernel, iterations=2)
        contours, _ = cv2.findContours(dilated, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        for cnt in contours:
            area = cv2.contourArea(cnt)
            if 100 < area < 5000:
                x, y, w, h = cv2.boundingRect(cnt)
                aspect = w / h
                if 0.3 < aspect < 3.0:
                    all_regions.append([x, y, x+w, y+h])
        if not all_regions:
            h, w = image.shape[:2]
            return (w//3, h//3, 2*w//3, 2*h//3)
        return self._get_tight_bounds(np.array(all_regions), image.shape)

    def _upscale_image(self, image: np.ndarray, scale_factor: int = 10) -> np.ndarray:
        if image is None or image.size == 0:
            return image
        pil_img = Image.fromarray(cv2.cvtColor(image, cv2.COLOR_BGR2RGB))
        new_size = (pil_img.size[0] * scale_factor, pil_img.size[1] * scale_factor)
        upscaled_pil = pil_img.resize(new_size, Image.Resampling.LANCZOS)
        return cv2.cvtColor(np.array(upscaled_pil), cv2.COLOR_RGB2BGR)

    def _crop_edges(self, image: np.ndarray, crop_size: int = 50) -> np.ndarray:
        if image is None or image.size == 0:
            return image
        h, w = image.shape[:2]
        if w <= 2*crop_size or h <= 2*crop_size:
            return image
        return image[crop_size:h-crop_size, crop_size:w-crop_size]

    def detect_and_crop(self, image_path: str, tracklet_name: str = None) -> dict:
        image = cv2.imread(image_path)
        if image is None:
            return {'success': False, 'cropped': None}
        h, w = image.shape[:2]

        # Strategy 1: Pose region
        if tracklet_name:
            pose_region = self._get_pose_region(image_path, tracklet_name)
            if pose_region:
                pose_crop = self._crop_from_pose(image, pose_region)
                results = self._detect_with_fallback(pose_crop)
                if results:
                    x1, y1, x2, y2 = pose_region
                    all_boxes = []
                    for bbox, text, conf in results:
                        pts = np.array(bbox, dtype=np.int32)
                        all_boxes.append([int(pts[:,0].min()) + x1, int(pts[:,1].min()) + y1,
                                         int(pts[:,0].max()) + x1, int(pts[:,1].max()) + y1])
                    if all_boxes:
                        bounds = self._get_tight_bounds(np.array(all_boxes), image.shape)
                        if bounds:
                            x1, y1, x2, y2 = bounds
                            cropped = image[y1:y2, x1:x2]
                            upscaled = self._upscale_image(cropped, 10)
                            final = self._crop_edges(upscaled, 50)
                            return {'success': True, 'cropped': final, 'original': cropped}

        # Strategy 2: Full image
        results = self._detect_with_fallback(image)
        if results:
            all_boxes = []
            for bbox, text, conf in results:
                pts = np.array(bbox, dtype=np.int32)
                all_boxes.append([int(pts[:,0].min()), int(pts[:,1].min()), int(pts[:,0].max()), int(pts[:,1].max())])
            if all_boxes:
                bounds = self._get_tight_bounds(np.array(all_boxes), image.shape)
                if bounds:
                    x1, y1, x2, y2 = bounds
                    cropped = image[y1:y2, x1:x2]
                    upscaled = self._upscale_image(cropped, 10)
                    final = self._crop_edges(upscaled, 50)
                    return {'success': True, 'cropped': final, 'original': cropped}

        # Strategy 3: Heuristic
        bounds = self._heuristic_crop(image)
        if bounds:
            x1, y1, x2, y2 = bounds
            cropped = image[y1:y2, x1:x2]
            upscaled = self._upscale_image(cropped, 10)
            final = self._crop_edges(upscaled, 50)
            return {'success': True, 'cropped': final, 'original': cropped, 'method': 'heuristic'}

        return {'success': False, 'cropped': None}

    def augment(self, cropped: np.ndarray) -> dict:
        if cropped is None or cropped.size == 0:
            return {}
        gray = cv2.cvtColor(cropped, cv2.COLOR_BGR2GRAY)
        versions = {'original': cropped}
        _, binary = cv2.threshold(gray, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
        versions['binary'] = cv2.cvtColor(binary, cv2.COLOR_GRAY2BGR)
        kernel = np.array([[-1,-1,-1], [-1,9,-1], [-1,-1,-1]])
        sharpened = cv2.filter2D(gray, -1, kernel)
        versions['sharpened'] = cv2.cvtColor(sharpened, cv2.COLOR_GRAY2BGR)
        return versions

    def process(self, image_path: str, tracklet_name: str = None) -> dict:
        result = self.detect_and_crop(image_path, tracklet_name)
        if not result['success']:
            return {'success': False, 'versions': {}}
        versions = self.augment(result['cropped'])
        return {'success': True, 'versions': versions, 'original_cropped': result['original'], 'method': result.get('method', 'easyocr')}

