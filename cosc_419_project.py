def load_ground_truth(gt_path: str) -> dict:
    """Load ground truth from JSON file."""
    with open(gt_path, 'r') as f:
        gt = json.load(f)
    return {str(k): str(v) for k, v in gt.items()}

def save_report(result: dict, output_folder: str):
    """Save prediction report to JSON file."""
    report_path = Path(output_folder) / f"{result['tracklet_id']}_prediction.json"
    with open(report_path, 'w') as f:
        json.dump(result, f, indent=2, default=str)

def calculate_accuracy(results: list, ground_truth: dict) -> dict:
    """Calculate comprehensive accuracy statistics."""
    stats = {
        'total_tracklets': len(results),
        'successful': 0, 'failed': 0, 'no_legible': 0,
        'correct': 0, 'incorrect': 0,
        'by_number': defaultdict(lambda: {'total': 0, 'correct': 0}),
        'predictions': [], 'confidence_scores': []
    }

    for r in results:
        gt = ground_truth.get(r['tracklet_id'], '-1')
        pred = r['prediction']
        is_correct = (pred == gt)

        stats['by_number'][gt]['total'] += 1
        if is_correct:
            stats['by_number'][gt]['correct'] += 1
            stats['correct'] += 1
        else:
            stats['incorrect'] += 1

        if r['status'] == 'success':
            stats['successful'] += 1
        elif r['status'] == 'no_legible_images':
            stats['no_legible'] += 1
        else:
            stats['failed'] += 1

        if r['confidence'] > 0:
            stats['confidence_scores'].append(r['confidence'])

        stats['predictions'].append({
            'tracklet_id': r['tracklet_id'],
            'ground_truth': gt, 'prediction': pred,
            'correct': is_correct, 'confidence': r['confidence'],
            'status': r['status']
        })

    stats['overall_accuracy'] = stats['correct'] / stats['total_tracklets'] * 100 if stats['total_tracklets'] > 0 else 0

    with_pred = [p for p in stats['predictions'] if p['prediction'] != '-1']
    stats['accuracy_when_predicted'] = (
        sum(1 for p in with_pred if p['correct']) / len(with_pred) * 100
        if with_pred else 0
    )

    stats['coverage'] = len(with_pred) / stats['total_tracklets'] * 100 if stats['total_tracklets'] > 0 else 0

    if stats['confidence_scores']:
        stats['avg_confidence'] = np.mean(stats['confidence_scores']) * 100
        stats['min_confidence'] = min(stats['confidence_scores']) * 100
        stats['max_confidence'] = max(stats['confidence_scores']) * 100

    stats['by_number'] = dict(stats['by_number'])
    return stats

