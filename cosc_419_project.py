class LegibilityStatus(Enum):
    LEGIBLE = "legible"
    BLURRY = "blurry"
    DARK = "dark"
    BRIGHT = "bright"
    OCCLUDED = "occluded"
    SMALL = "small"
    UNKNOWN = "unknown"

# ============================================================================
# HEURISTIC CLASSIFIER
# ============================================================================
class HeuristicLegibilityClassifier:
    def __init__(self, verbose: bool = True, **kwargs):
        self.verbose = verbose
        self.blur_threshold = kwargs.get('blur_threshold', Config.BLUR_THRESHOLD)
        self.min_brightness = kwargs.get('min_brightness', Config.MIN_BRIGHTNESS)
        self.max_brightness = kwargs.get('max_brightness', Config.MAX_BRIGHTNESS)
        self.min_contrast = kwargs.get('min_contrast', Config.MIN_CONTRAST)
        self.min_score_threshold = kwargs.get('min_score_threshold', Config.MIN_SCORE_THRESHOLD)
        self.score_history = []
        if verbose:
            print(f"📏 Heuristic thresholds initialized")

    def classify(self, img_path: Union[str, Path]) -> Dict[str, Any]:
        try:
            img = cv2.imread(str(img_path))
            if img is None:
                return self._error_result(str(img_path), "Could not read image")
            gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
            blur_score = cv2.Laplacian(gray, cv2.CV_64F).var()
            brightness = np.mean(gray)
            contrast = np.std(gray)
            edges = cv2.Canny(gray, 50, 150)
            jersey_size = np.sum(edges > 0) / edges.size * 100
            has_text = self._detect_text_regions(gray)
            score = self._calculate_score(blur_score, brightness, contrast, jersey_size, has_text)
            self.score_history.append(score)
            status, legible = self._determine_status(blur_score, brightness, contrast, jersey_size, score)
            return {
                'image': str(img_path), 'legible': bool(legible), 'status': status.value,
                'score': float(score),
                'metrics': {'blur_score': float(blur_score), 'brightness': float(brightness),
                           'contrast': float(contrast), 'jersey_size_ratio': float(jersey_size),
                           'has_text_region': bool(has_text)}
            }
        except Exception as e:
            return self._error_result(str(img_path), str(e))

    def _error_result(self, img_path: str, error: str) -> Dict[str, Any]:
        return {'image': img_path, 'legible': False, 'status': LegibilityStatus.UNKNOWN.value,
                'score': 0.0, 'metrics': {}, 'error': error}

    def _detect_text_regions(self, gray: np.ndarray) -> bool:
        try:
            thresh = cv2.adaptiveThreshold(gray, 255, cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
                                          cv2.THRESH_BINARY_INV, 11, 2)
            kernel = cv2.getStructuringElement(cv2.MORPH_RECT, (3, 3))
            eroded = cv2.erode(cv2.dilate(thresh, kernel, iterations=2), kernel, iterations=1)
            contours, _ = cv2.findContours(eroded, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
            img_area = gray.size
            for contour in contours:
                area = cv2.contourArea(contour)
                if 50 < area < img_area * 0.15:
                    x, y, w, h = cv2.boundingRect(contour)
                    if 0.3 < w / float(h) < 3.0:
                        return True
            return False
        except:
            return False

    def _calculate_score(self, blur: float, brightness: float, contrast: float,
                        jersey_size: float, has_text: bool) -> float:
        blur_norm = min(blur / self.blur_threshold, 1.0)
        brightness_norm = max(0.0, min(1.0, 1.0 - abs(brightness - 127.5) / 127.5))
        contrast_norm = min(contrast / 60.0, 1.0)
        size_norm = min(jersey_size / 10.0, 1.0)
        return min(1.0, (Config.WEIGHT_BLUR * blur_norm + Config.WEIGHT_BRIGHTNESS * brightness_norm +
                        Config.WEIGHT_CONTRAST * contrast_norm + Config.WEIGHT_SIZE * size_norm +
                        (Config.TEXT_BONUS if has_text else 0.0)))

    def _determine_status(self, blur: float, brightness: float, contrast: float,
                         jersey_size: float, score: float) -> Tuple[LegibilityStatus, bool]:
        if blur < self.blur_threshold * 0.4:
            return LegibilityStatus.BLURRY, False
        if brightness < self.min_brightness:
            return LegibilityStatus.DARK, False
        if brightness > self.max_brightness:
            return LegibilityStatus.BRIGHT, False
        if contrast < self.min_contrast:
            return LegibilityStatus.OCCLUDED, False
        if jersey_size < 3.0:
            return LegibilityStatus.SMALL, False
        legible = score >= self.min_score_threshold
        return (LegibilityStatus.LEGIBLE if legible else LegibilityStatus.UNKNOWN), bool(legible)

    def get_score_statistics(self) -> Dict[str, float]:
        if not self.score_history:
            return {}
        scores = np.array(self.score_history)
        return {'mean': float(np.mean(scores)), 'median': float(np.median(scores)),
                'std': float(np.std(scores)), 'min': float(np.min(scores)),
                'max': float(np.max(scores)), 'count': len(scores)}

