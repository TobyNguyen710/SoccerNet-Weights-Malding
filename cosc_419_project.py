        print(f"\n📋 PER-NUMBER PERFORMANCE")
        print(f"{'─'*50}")
        print(f"Number | Total | Correct | Accuracy")
        print(f"{'─'*50}")
        for num in sorted(stats['by_number'].keys(),
                         key=lambda x: int(x) if x.isdigit() and x != '-1' else float('inf')):
            data = stats['by_number'][num]
            acc = data['correct']/data['total']*100 if data['total'] else 0
            print(f"{num:>6} | {data['total']:5} | {data['correct']:7} | {acc:6.1f}%")
